def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

def multiplicacion(a, b):
    return a * b

def division(a, b):
    if b == 0:
        return "Error: Division por cero"
    return a / b

def potenciacion (a, b):
    return a ** b

def division_entera(a, b):
    if b == 0:
        return "Error: Division por cero"
    return a // b
